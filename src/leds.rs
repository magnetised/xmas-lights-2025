pub struct LEDs {
    brightness: f32,
    leds: Ws2812Rpi,
    data: Vec<RGB8>,
}

impl LEDs {
    pub fn new(brightness: f32) -> Self {
        let ws = Ws2812Rpi::new(NUM_LEDS as i32, PIN).unwrap();
        LEDs {
            brightness,
            leds: ws,
            data: vec![RGB8::default(); NUM_LEDS],
        }
    }
    fn set_colour(&mut self, l: usize, rgb: Rgb) {
        let (r, g, b) = rgb;
        self.data[l].r = r;
        self.data[l].g = g;
        self.data[l].b = b;
    }
}

impl Display for LEDs {
    fn render(&mut self, grid: &Array2D<Rgb>) {
        let map = ARRAY_MAP;
        for y in 0..HEIGHT {
            for x in 0..WIDTH {
                let l = map[y][x];
                let (r, g, b) = *grid.get(y, x).unwrap();
                if self.brightness < 1.0 {
                    let hsv = rgb_to_hsv(Rgba {
                        r: r as f32 / 255.0,
                        b: b as f32 / 255.0,
                        g: g as f32 / 255.0,
                        a: 1.0,
                    });
                    let rgba = hsv_to_rgb(HSVa {
                        v: hsv.v * self.brightness,
                        ..hsv
                    });
                    self.set_colour(
                        l,
                        (
                            (rgba.r * 255.0).round() as u8,
                            (rgba.g * 255.0).round() as u8,
                            (rgba.b * 255.0).round() as u8,
                        ),
                    );
                } else {
                    self.set_colour(l, (r, g, b));
                }
            }
        }
        smart_leds::SmartLedsWrite::write(
            &mut self.leds,
            smart_leds::gamma(self.data.iter().copied()), // self.data.iter().copied(),
        )
        .unwrap();
    }
}
